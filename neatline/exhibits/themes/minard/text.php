<h1>Figurative Map of the successive losses in men of the French Army in the
    Russian campaign 1812-1813.</h1>

<h2>Drawn up by M. Minard, Inspector General of Bridges and Roads in
    retirement.</h2>

<h3>Paris, November 20, 1869.</h3>

<p>The numbers of men present are represented by the widths of the colored zones at a rate of one millimeter for every ten-thousand men; they are further written across the zones. The red designates the men who enter into Russia, the black those who leave it. The information which has served to draw up the map has been extracted from the works of M. M. Thiers, of Segur, of Fezensac, of Chambray, and the unpublished diary of Jacob, pharmacist of the army since October 28th. In order to better judge with the eye the diminution of the army, I have assumed that the troops of prince Jerome and of Marshal Davoush who had been detached at Minsk and Moghilev and have rejoined around Orcha and Vitebsk, had always marched with the army.</p>
