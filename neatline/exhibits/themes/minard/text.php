<p><span data-neatline-slug="s4">The numbers of men present are represented by the widths of the colored zones at a rate of one millimeter for every ten-thousand men; they are further written across the zones</span>. <span data-neatline-slug="s5">The red designates the men who enter into Russia, the black those who leave it</span>. &mdash; <span data-neatline-slug="s6">The information which has served to draw up the map has been extracted from the works of M. M. Thiers, of Segur, of Fezensac, of Chambray, and the unpublished diary of Jacob, pharmacist of the army since October 28th</span>. <span data-neatline-slug="s7">In order to better judge with the eye the diminution of the army, I have assumed that the troops of prince Jerome and of Marshal Davoush who had been detached at Minsk and Moghilev and have rejoined around Orcha and Vitebsk, had always marched with the army</span>.</p>

